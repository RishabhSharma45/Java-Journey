package litrals;

public class Litrals {
     public static void main(String[] args) {
    	 //in java litrals can be of many form 
    	 
    	 //decimal form     base 10  0-9
    	 int      x10 =       100;
  //datatype   Identifier      litral - value assigned to a identifier
    	 
    	//Octal form     base 8  0-7
    	 int      x8 =       0100; //starts with 0
   //datatype   Identifier  litral - value assigned to a identifier
    	 
    	 //hexadecimsl form  base 16  0-15   F
    	 int x16 = 0x1af;
    	 
    	 System.out.println(x10 +" "+x8 + " "+x16);
     }
}
